void oled_render_left(void) {
    oled_write_P(PSTR("Layer\n"), false);
    switch (get_highest_layer(layer_state)) {
        case 0: oled_write_P(PSTR("Base\n"),   false); break;
        case 1: oled_write_P(PSTR("Lower\n"),  false); break;
        case 2: oled_write_P(PSTR("Raise\n"),  false); break;
        case 3: oled_write_P(PSTR("Adjust\n"), false); break;
        default: oled_write_P(PSTR("???\n"),   false); break;
    }
    oled_write_P(PSTR("---\n"), false);
    oled_write_P(PSTR("RGB\n"), false);
    oled_write_P(rgb_effect_name(), false);
}


static uint8_t anim_frame = 0;
static uint32_t last_anim_time = 0;

void oled_render_right(void) {
    uint8_t wpm = get_current_wpm();

    uint32_t frame_ms;
    if      (wpm < 20)  frame_ms = 800;
    else if (wpm < 40)  frame_ms = 500;
    else if (wpm < 70)  frame_ms = 300;
    else if (wpm < 100) frame_ms = 150;
    else                frame_ms = 60;

    if (timer_elapsed32(last_anim_time) > frame_ms) {
        anim_frame = (anim_frame + 1) % 3;
        last_anim_time = timer_read32();
    }

    switch (anim_frame) {
        case 0: oled_write_raw_P(frame1, sizeof(frame1)); break;
        case 1: oled_write_raw_P(frame2, sizeof(frame2)); break;
        case 2: oled_write_raw_P(frame3, sizeof(frame3)); break;
    }

    // WPM por cima do sprite no topo
    char wpm_str[8];
    snprintf(wpm_str, sizeof(wpm_str), "WPM:%3d", wpm);
    oled_set_cursor(0, 0);
    oled_write(wpm_str, false);
}

bool oled_task_user(void) {
    if (is_keyboard_master()) oled_render_left();
    else oled_render_right();
    return false;
}

char keylog_str[24] = {};
const char code_to_name[60] = {
    ' ',' ',' ',' ','a','b','c','d','e','f','g','h','i','j','k','l','m','n','o','p',
    'q','r','s','t','u','v','w','x','y','z','1','2','3','4','5','6','7','8','9','0',
    'R','E','B','T','_','-','=','[',']','\\','#',';','\'','`',',','.','/',' ',' ',' '};

void set_keylog(uint16_t keycode, keyrecord_t *record) {
    char name = ' ';
    if ((keycode >= QK_MOD_TAP && keycode <= QK_MOD_TAP_MAX) ||
        (keycode >= QK_LAYER_TAP && keycode <= QK_LAYER_TAP_MAX)) keycode = keycode & 0xFF;
    if (keycode < 60) name = code_to_name[keycode];
    snprintf(keylog_str, sizeof(keylog_str), "%dx%d k%2d:%c",
             record->event.key.row, record->event.key.col, keycode, name);
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) set_keylog(keycode, record);
    return true;
}
